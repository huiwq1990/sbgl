package com.sbgl.app.actions.equipment.template;

public class ClassficationCourse {
	private String id;
	private String name;
	private String pId;
	private String pName;
	private String modelCount;
	private String equipCount;
	private String isParent;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getpId() {
		return pId;
	}
	public void setpId(String pId) {
		this.pId = pId;
	}                                                                                                                   
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getModelCount() {
		return modelCount;
	}
	public void setModelCount(String modelCount) {
		this.modelCount = modelCount;
	}
	public String getEquipCount() {
		return equipCount;
	}
	public void setEquipCount(String equipCount) {
		this.equipCount = equipCount;
	}
	public String getIsParent() {
		return isParent;
	}
	public void setIsParent(String isParent) {
		this.isParent = isParent;
	}
	
}
