package com.sbgl.app.actions.equipment;

public class ClassficationCourse {
	private String id;
	private String name;
	private String pName;
	private String modelCount;
	private String equipCount;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getModelCount() {
		return modelCount;
	}
	public void setModelCount(String modelCount) {
		this.modelCount = modelCount;
	}
	public String getEquipCount() {
		return equipCount;
	}
	public void setEquipCount(String equipCount) {
		this.equipCount = equipCount;
	}
	
}
